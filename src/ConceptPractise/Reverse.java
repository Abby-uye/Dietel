package ConceptPractise;

public class Reverse {
    public static void main(String[] args) {
        for (int count =1;count<=10;count++){
            System.out.printf(" %d %n",count);
            for (int count1 =10;count1 > 1 ;count1--){
                System.out.print(count1);
            }
        }
    }
}
